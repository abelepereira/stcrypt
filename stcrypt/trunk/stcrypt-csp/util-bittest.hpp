namespace stcrypt {

	template <class T>
	bool test_mask(T const v, T const mask)
	{
		return ( (v & mask) == mask);
	}

	template <class T>
	bool test_if_any_out_of_mask(T const v, T const mask)
	{
		return test_if_any_in_mask<T>(v,  static_cast<T>(~mask) );
	}

	template <class T>
	bool test_if_any_in_mask(T const v, T const mask)
	{
		return ( (v & mask) !=0);
	}

}
